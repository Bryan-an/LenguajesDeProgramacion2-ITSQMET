    private void txt_userMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txt_userMousePressed
        if (txt_user.getText().equals(defaultUser)) {
            txt_user.setText("");
            txt_user.setForeground(Color.BLACK);
        }
    }//GEN-LAST:event_txt_userMousePressed

    private void pwd_passwordMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_pwd_passwordMousePressed
        if (pwd_password.getText().equals(defaultPassword)) {
            pwd_password.setText("");
            pwd_password.setForeground(Color.BLACK);
        }
    }//GEN-LAST:event_pwd_passwordMousePressed

    private void txt_userFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txt_userFocusLost
        if (txt_user.getText().isEmpty()) {
            txt_user.setText(defaultUser);
            txt_user.setForeground(new Color(204, 209, 209));
        }
    }//GEN-LAST:event_txt_userFocusLost

    private void pwd_passwordFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_pwd_passwordFocusLost
        if (pwd_password.getText().isEmpty()) {
            pwd_password.setText(defaultPassword);
            pwd_password.setForeground(new Color(204, 209, 209));
        }
    }//GEN-LAST:event_pwd_passwordFocusLost

