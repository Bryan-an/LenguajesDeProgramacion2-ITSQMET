    private void btn_logInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_logInActionPerformed
        if (LogInController.validateFields(txt_user, pwd_password, bgp_userType)) {
            if (LogInController.exists(txt_user, pwd_password, userType)) {
                LogInController.getInto();
                dispose();
            } else {
                JOptionPane.showMessageDialog(this,
                        "El usuario, la contraseña o el tipo de usuario es incorrecto",
                        "Información inválida",
                        JOptionPane.ERROR_MESSAGE,
                        Icons.ERROR_MESSAGE);

                clear();
            }
        } else {
            JOptionPane.showMessageDialog(this,
                    "Todos los campos son obligatorios",
                    "Información incompleta",
                    JOptionPane.ERROR_MESSAGE,
                    Icons.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btn_logInActionPerformed

