    /**
     * Displays the invoice and the total.
     *
     * @param detailsTable The table that contains the purchase details.
     * @param subtotalLabel The label that contains the purchase subtotal.
     * @param mainFrame The main frame.
     * @param invoiceNumber The invoice number.
     */
    public void displayInvoice(JTable detailsTable, JLabel subtotalLabel, JFrame mainFrame, int invoiceNumber) {
        double subtotal;
        double iva;
        double total;
        this.mainFrame = mainFrame;

        for (int i = 0; i < detailsTable.getRowCount(); i++) {
            invoiceModel.addRow(new Object[]{
                detailsTable.getValueAt(i, 0),
                detailsTable.getValueAt(i, 4),
                detailsTable.getValueAt(i, 5),
                detailsTable.getValueAt(i, 6),});
        }

        txt_subtotal.setText(subtotalLabel.getText());

        try {
            subtotal = formatter.parse(subtotalLabel.getText()).doubleValue();
            iva = subtotal * 0.12;
            total = subtotal + iva;

            txt_iva.setText(formatter.format(iva));
            txt_total.setText(formatter.format(total));
        } catch (ParseException ex) {
            System.out.println(ex);
        }

        lbl_invoiceTitle.setText("Factura N° " + invoiceNumber);
    }

