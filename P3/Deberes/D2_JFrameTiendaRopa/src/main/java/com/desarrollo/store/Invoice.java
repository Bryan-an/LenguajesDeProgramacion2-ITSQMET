    private void btn_sendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_sendActionPerformed
        boolean validDocument = txt_document.getText().matches("\\d{10}");
        boolean validEmail = txt_email.getText().matches("^[_A-Za-z0-9-\\+]+(\\.[_A-Za-z0-9-]+)*@[A-Za-z0-9-]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$");

        if (txt_name.getText().isEmpty()
                || txt_document.getText().isEmpty()
                || txt_email.getText().isEmpty()
                || txt_address.getText().isEmpty()) {

            JOptionPane.showMessageDialog(this, "Información incompleta",
                    "Error", JOptionPane.ERROR_MESSAGE);

        } else {

            if (validDocument) {
                if (validEmail) {

                    JOptionPane.showMessageDialog(this, "La factura se ha enviado satisfactoriamente",
                            "Enviado", JOptionPane.INFORMATION_MESSAGE);

                    dispose();
                    mainFrame.setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(this, "Correo electrónico inválido",
                            "Error", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Número de cédula inválida",
                        "Error", JOptionPane.ERROR_MESSAGE);
            }

        }

    }//GEN-LAST:event_btn_sendActionPerformed

