    public void calcularTotal() {
        double totalPagar, precio, cantidad;

        precio = Double.parseDouble(jTxtPrecio.getText());
        cantidad = Double.parseDouble(jTxtCantidad.getText());

        if (jTxtMarca.getText().equalsIgnoreCase("Vans")) {
            totalPagar = (precio * cantidad) - (precio * cantidad * 0.10);
        } else if (jTxtMarca.getText().equalsIgnoreCase("Nike")) {
            totalPagar = (precio * cantidad) - (precio * cantidad * 0.08);
        } else if (jTxtMarca.getText().equalsIgnoreCase("Adidas")) {
            totalPagar = (precio * cantidad) - (precio * cantidad * 0.05);
        } else {
            totalPagar = (precio * cantidad) - (precio * cantidad * 0.02);
        }

        jTxtTotalPagar.setText(String.format("%.2f", totalPagar));

    }

    public void limpiar() {
        jTxtMarca.setText("");
        jTxtPrecio.setText("");
        jTxtCantidad.setText("");
    }

